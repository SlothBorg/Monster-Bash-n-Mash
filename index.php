    <section class="mb" id="plan">
      <details>
        <summary class="big-summary">The Plan</summary>
        <section class="bb pl pr">
          <p>The basic plan has four phases</p>
          <p><emphasis>1. Create inspiration cards:</emphasis></p>
          <p>I invite you all to get some paper and draw an image. Write down a word/phrase to serve as inspiration, these can be silly, strange, scary, sexy, or some other word starting with s. Or if you see something cool our in the world, take a picture of it, #NoFilter</p>
          <p><emphasis>For example:</emphasis><br>
            <ul>
              <li>drawings of: bones, horns, scales, a tail, a mouth etc.</li>
              <li>words/phrases such as: Bone, Gnarly, bulbous, oozing, shimmering, pulsating pustules etc.</li>
              <li>photos of: a cool stick, some gnarly bark, a weird flower etc.</li>
            </ul>
          </p>
          <p>Ideally this part would be completed before the party weekend, that way I can make a virtual deck of the cards for those not able to attend.</p>
          <p><emphasis>Upload your cards <a href="https://drive.google.com/drive/folders/10ZXJiIRq5Dniw0bhOPKQKpZ0S1Hx3Q3e?usp=sharing" target="_blank" rel="noreferrer">here</a></emphasis> I've already uploaded a few cards of my own. <small>I set up a Google Drive folder just for this, please make use of it, I don't want to have inconvenienced those many electrons for nothing.</small></p>
        </section>

        <section class="bb pl pr">
          <p><emphasis>2. Drawing Cards:</emphasis> aka determining your fate</p>
          <p>Each person draws some number, that number will be decide as a group, though you are welcome to draw more cards! Each card can be interpreted how ever you see fit.</p>
        </section>

        <section class="bb pl pr">
          <p><emphasis>3. MAKE MONSTERS:</emphasis> aka The Really Fun Part!</p>
          <p>Using the cards you drew, make something!, It could be a sculpture, a miniature, a mosaic, collage, painting, poem, a program, website a short story or what ever else you feel moved to create!</p>
        </section>

        <section class="bb pl pr">
          <p><emphasis>4. Show and Tell:</emphasis></p>
          <p>Share what you made, take pictures of it, talk about it, etc.</p>
        </section>
    </section>

