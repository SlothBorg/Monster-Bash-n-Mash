        <summary class="big-summary">What?</summary>
        <section class="pl pr">
          <blockquote class="center"><em>The monster mash, it was a graveyard smash</em></blockquote>
          <p>Er... no not a <em>Mash</em>, a <em>Bash</em>!</p>
          <p>So in 2020 a <a href="https://www.youtube.com/channel/UCUtsRCYEE-hCPsftezm9wzA" class="hidden-link" target="_blank" rel="noreferrer">YouTuber</a> Sherdyl && I like, got some of his friends together and they each created a few cards depicting monster parts.</p>
          <p>They then picked 5 cards, from the deck they'd all contributed too, and use those cards as inspiration for making Monsters!</p>
          <p>Earlier this year they did it again, but with more people!</p>
